package demos;

public class arthimetic {
	public static void main (String[] args){
		int result = 1 + 2;
		System.out.println(result);
		
		result--;
		System.out.println(result);
		
		result *= 2;
		System.out.println(result);
		
		result /= 2;
		System.out.println(result);
		
		result += 8;
		result %= 7;
		System.out.println(result);
	}
}
